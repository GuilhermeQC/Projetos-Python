#Idade calculo

print ("Para calcular a idade de uma pessoa é necessário subtrair o ano de seu nascimento do ano atual!")

AA = int(input("Insira o ano atual em que se encontra \n"))
AN = int(input("Insira o ano de nascimento \n"))
Idade = AA - AN

print (f"A idade resultante do calculo, considerando que ja foi feito aniversário esse ano, é de: {Idade} anos!")