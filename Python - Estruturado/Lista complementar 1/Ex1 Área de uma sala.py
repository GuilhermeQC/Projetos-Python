# Programa 1 - Área de uma sala

print("Para calcular a área de uma sala retângular será necessário duas medidas, Largura e Comprimento")

L = float(input("Digite a largura da sala: \n"))
C = float(input("Digite o comprimento da sala \n"))
Area = C*L

print(f"A sala tem á area de: {Area} m²")
